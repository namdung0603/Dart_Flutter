/*
- sử dụng "var" để dart tự suy luận kiểu
- khai báo biến cụ thể
- khai báo dạng object
*/
void main() {
  //Tu suy luan kieu
  var name = "Tran Nam Dung";
  var age = 18;
  //Khai bao bien cu the
  String address = "Ha Noi";
  int phone = 123456789;
  //Khai vbao dang object
  Object userName = "DungTN";
  print(
    "Name: $name, Age: $age, Address: $address, Phone: $phone, UserName: $userName",
  );
}
